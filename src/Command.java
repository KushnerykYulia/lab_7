/**
 * Інтерфейс Command
 * Описує метод execute, який буде реалізовано кожною конкретною командою.
 */
interface Command {
    /**
     * Метод виконує дію, наприклад, малювання чи стирання, за заданими координатами.
     * @param x координата X натискання миші
     * @param y координата Y натискання миші
     */
    void execute(int x, int y);
}
